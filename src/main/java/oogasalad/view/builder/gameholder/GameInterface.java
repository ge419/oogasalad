package oogasalad.view.builder.gameholder;

import oogasalad.view.builder.board.ImmutableBoardInfo;
import oogasalad.view.builder.graphs.ImmutableGraph;


/**
 * The GameInterface will implement the API of Game objects that allows you to receive data directly
 * related to the game and how it functions.
 *
 * @author tmh85
 */
public interface GameInterface {

  /**
   * Gets you an immutable graph object, an object that contains all the tiles and how they connect
   * with each other.
   *
   * @return ImmutableGraph
   */
  ImmutableGraph getTileGraph();

  /**
   * Gets you an immutable board info object, an object that contains all images for a board along
   * with their sizes and locations, along with the size of the board itself.
   *
   * @return ImmutableBoardInfo
   */
  ImmutableBoardInfo getBoardInfo();

  String getGameFilePath();

  // TODO: IMPLEMENT WAY TO ACCESS RULES
}
