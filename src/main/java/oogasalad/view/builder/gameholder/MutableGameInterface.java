package oogasalad.view.builder.gameholder;

import oogasalad.view.builder.board.ImmutableBoardInfo;
import oogasalad.view.builder.graphs.ImmutableGraph;

/**
 * MutableGameInterface provides the API for how to manipulate a game object. Note that only classes
 * that need to modify a game object should use a class that implements these methods.
 *
 * @author tmh85
 */
public interface MutableGameInterface {

  /**
   * Sets the graph for this game. Note that we require the graph to be immutable.
   *
   * @param graph an immutable graph object
   */
  void setTileGraph(ImmutableGraph graph);

  /**
   * Sets the BoardInfo object for this game. Note that we require this BoardInfo to be immutable.
   *
   * @param board an immutable BoardInfo object
   */
  void setBoardInfo(ImmutableBoardInfo board);

  // TODO: IMPLEMENT WAY TO MODIFY RULES
}
